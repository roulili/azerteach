            <h3>Des ateliers d’initiation aux sciences</h3>
            <p>
            Nos ateliers d’initiation aux sciences s’adressent à quiconque, quel que soit son niveau, désire <strong>apprendre les sciences</strong> de façon ludique.
			Animés par un professeur de sciences compétent, les participants sont progressivement initiés aux principales matières scientifiques
			au travers d’<strong>expériences pratiques</strong> et variées.
			Par exemple, la physique mécanique, la chimie organique, la biologie moléculaire, et autres.
            </p>

            <p>
            L’objectif de ces ateliers d’initiation aux sciences est de montrer les applications concrètes des savoirs en physique, chimie et biologie,
			qui peuvent paraître, à bien des égards, relativement complexes et abstraits.
			De ce point de vue, ils se présentent comme un <strong>complément idéal aux cours de sciences</strong> tels qu’enseignés à l’école.
            </p>
            <p>
            Les ateliers d’initiation aux sciences ont lieu dans le cadre de nos stages de vacances.
			Selon vos envies, vous pouvez cumuler cet atelier avec un autre choisi parmi l’ensemble de nos activités organisées en cette occasion.
