            <h3>Cours particuliers</h3>
            <p>
           	Quel que soit votre niveau, vous pouvez faire appel <strong>à tout moment</strong> à l’un de nos professeurs de français en vue d’un cours particulier.
		  	Celui-ci adapte son enseignement selon vos attentes et vos besoins.
			Lors de votre premier cours de français, vous définissez les points de matière plus compliqués et dressez un planning pour les jours ou semaines à venir.
            </p>
            <p>
            Les cours particuliers de français conviennent particulièrement à ceux et celles qui souhaitent <strong>faire le point</strong>, de façon intensive,
			sur leur connaissance de la langue (orthographe, grammaire, conjugaison) en vue d’un examen à l’école ou à l’université.
			Nos cours conviennent aussi pour la préparation d'un test standardisé tel que celui dispensé par l’Alliance française.
            </p>
            <p>
            Par ailleurs, elle permet aux primo-arrivants (enfants, adolescents, adultes confondus), qui souhaitent <strong>apprendre le français</strong>, de bénéficier d’une formation accélérée.
			Au terme de son parcours, chaque élève maîtrisera les règles fondamentales de l’orthographe et de la grammaire françaises,
			sera capable de s’exprimer en des termes simples et de comprendre des dialogues tirés de la vie quotidienne.
            </p>
            <p>
              L’horaire des cours particuliers de français est relativement souple. Selon vos disponibilités, vous fixez le jour ainsi que l’heure et la durée de chacun de vos cours.
            </p>
            <p>
              A l’instar des études encadrées, les <a href="../cours-particuliers">cours particuliers</a> de néerlandais sont l’occasion pour l’élève de:
            </p>
            <ul class="blue-dot-grey-text">
              <li><span class="text-grey">Bénéficier de <strong>nouvelles explications</strong> adaptées à ses modes d’apprentissage</span></li>
              <li><span class="text-grey">De pratiquer dans un <strong>cadre rassurant</strong> pour les plus timides</span></li>
              <li><span class="text-grey">Découvrir et d’adopter une <strong>méthode de travail</strong> efficace et qui permette de progresser de façon autonome.</span></li>
            </ul>
